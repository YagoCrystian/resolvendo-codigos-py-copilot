# Recebe dois dados diferentes do usuário
dado_usuario1 = input("Digite o seu primeiro nome: ")
dado_usuario2 = input("Digite o seu sobrenome: ")

# Concatena os dados em uma única string
nome_completo = f"{dado_usuario1} {dado_usuario2}"

# Imprime a string concatenada
print(f"O seu nome completo é {nome_completo}?")
